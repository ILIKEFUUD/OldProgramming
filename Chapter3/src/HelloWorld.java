public class HelloWorld { // class heading
		public static void main (String [] args){
			
			System.out.println("Hello World!!!");
			System.out.println(); //puts a blank line between the strings
			System.out.println("Rahul Shah");
			// Escape sequence characters
			// \n new line- moves output to next line
			// \" puts a quote 
			// \\ puts a \
			//t tabs the string
			System.out.println("\\\\\\ \"Can't wait\n until Friday\" ///");
			System.out.print("Clarksburg"); //System.out.print doesn't move to the next line.
			System.out.println("High");
			System.out.println("School");
		}//end of main



} // End of HelloWorld