public class RectanglePerimiter {
	public static void main (String [] args){
		
		int length = 11;
		int width = 5;
		int perimiter;
		
		perimiter = (2*length)+(2*width);
		
		System.out.println("The perimiter is " + perimiter);
		
		
		
	}//end of main
}//end of RectanglePerimiter