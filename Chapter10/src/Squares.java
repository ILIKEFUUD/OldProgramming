public class Squares {

	
	public static void main(String[] args) {
		
		int [] square = new int [5];
		
		for (int x = 0; x < 5; x++){
			square[x] = x*x;
		}
		
		for (int x = 0; x < 5; x++){
			System.out.println(square[x]);
		}

	}//end of main

}//end of Squares